CREATE TABLE matrizm (
	distance0 INT NOT NULL,
	distance1 INT NOT NULL,
	distance2 INT NOT NULL,
	distance3 INT NOT NULL,
	distance4 INT NOT NULL,
	distance5 INT NOT NULL,
	distance6 INT NOT NULL,
	distance7 INT NOT NULL,
	distance8 INT NOT NULL,
	distance9 INT NOT NULL,
	distance10 INT NOT NULL,
	distance11 INT NOT NULL,
	distance12 INT NOT NULL
);

CREATE TABLE matrizt (
	distance0 INT NOT NULL,
	distance1 INT NOT NULL,
	distance2 INT NOT NULL,
	distance3 INT NOT NULL,
	distance4 INT NOT NULL,
	distance5 INT NOT NULL,
	distance6 INT NOT NULL,
	distance7 INT NOT NULL,
	distance8 INT NOT NULL,
	distance9 INT NOT NULL,
	distance10 INT NOT NULL,
	distance11 INT NOT NULL,
	distance12 INT NOT NULL
);

CREATE TABLE estaciones (
	pk_estacionid INT NOT NULL,
	linea INT NOT NULL,
	numero INT NOT NULL,
	nombre varchar(100) NOT NULL,
	tipo INT NOT NULL,
	status INT NOT NULL,
	PRIMARY KEY(pk_estacionid)
);

CREATE TABLE puntosinteres (
	fk_estacionid INT NOT NULL,
	nombre varchar(500) NOT NULL,
	descripcion varchar(500),
	FOREIGN KEY(fk_estacionid)
	REFERENCES estaciones(pk_estacionid)
);


